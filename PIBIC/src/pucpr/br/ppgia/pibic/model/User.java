package pucpr.br.ppgia.pibic.model;

public class User {
	
	private int id;
	private String user;
	
	public User()
	{
		
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
}
